package hehe;

public class TripleFromMongo {
	private String _id;
	private String timestamp;
	private String o;
	private String p;
	private String s;
	
	public String getId(){
		return _id;
	}
	
	public void setId (String _id){
		this._id=_id;
	}
	
	public String getTimestamp(){
		return timestamp;
	}
	
	public void setTimestamp(String timestamp){
		this.timestamp=timestamp;
	}
	
	public String getO(){
		return o;
	}
	
	public void setO(String o){
		this.o=o;
	}
	
	public String getP(){
		return p;
	}
	
	public void setP(String p){
		this.p=p;
	}
	
	public String getS(){
		return s;
	}
	
	public void setS(String s){
		this.s=s;
	}
	
	
}
